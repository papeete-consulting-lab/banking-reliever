# Source-context prefix on bcm asset IDs (CLI v2.0.0+ namespacing): a two-token
# <ENTERPRISE>.<SCOPE>. head (e.g. BNK.RLVR. / BNK.TECH.) that precedes a known
# asset-type token. The lookahead keeps process-authored tactical IDs
# (AGG.BSP.… / CMD.BSP.… / POL.… / PRJ.… / QRY.…) untouched — only
# CAP/RVT/EVT/OBJ/SUB/RES/CON carry the prefix.
_SRC_CTX_RE = re.compile(
    r"^[A-Z]{2,}\.[A-Z]{2,}\.(?=(?:CAP|RVT|EVT|OBJ|SUB|RES|CON)\.)"
)
# A capability folder name is one that ends with the CAP.<…> token, whether or
# not it carries a source-context prefix (CAP.BSP.001.SCO | BNK.RLVR.CAP.BSP.001.SCO).
_CAP_DIR_RE = re.compile(r"(?:^|\.)CAP\.[A-Z0-9.]+$")
# Capture a full (optionally prefixed) resource-event ID anywhere in a string.
_RVT_ID_RE = re.compile(r"((?:[A-Z]{2,}\.[A-Z]{2,}\.)?RVT\.[A-Z0-9._-]+)")


def _split_src_ctx(asset_id: str) -> tuple[str, str]:
    """Split a bcm asset id into (source-context-prefix, core).

    'BNK.RLVR.RVT.BSP.001.X' -> ('BNK.RLVR.', 'RVT.BSP.001.X')
    'EVT.REF.001.X'          -> ('', 'EVT.REF.001.X')
    'AGG.BSP.001.SCO.FOO'    -> ('', 'AGG.BSP.001.SCO.FOO')   # tactical, untouched
    """
    m = _SRC_CTX_RE.match(asset_id)
    return (asset_id[: m.end()], asset_id[m.end():]) if m else ("", asset_id)


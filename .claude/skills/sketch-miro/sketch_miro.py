@dataclass
class Capability:
    cap_id: str
    aggregates: list[dict]
    commands: list[dict]
    policies: list[dict]
    read_models: list[dict]   # both PRJ.* and QRY.* flattened
    bus: dict
    name: str = ""

    @property
    def short_id(self) -> str:
        return self.cap_id


def _run_bcm_pack_process(*args: str) -> tuple[int, str, str]:
    """Invoke `rlv-knowledge process <args>` and return (returncode, stdout, stderr).

    The leading '# knowledge-base …' provenance line and any '[rlv-knowledge] …'
    log lines are emitted on stderr by the CLI, so stdout is the clean
    payload (a cap-id list for `--list`, JSON for `<CAP> --compact`)."""
    try:
        proc = subprocess.run(
            ["rlv-knowledge", "process", *args],
            capture_output=True, text=True, check=False,
        )
    except FileNotFoundError as exc:
        raise RuntimeError(
            "rlv-knowledge is not installed / not on PATH — cannot fetch the "
            "process model (it lives in the reliever-knowledge repo, served "
            "via `rlv-knowledge process`)."
        ) from exc
    return proc.returncode, proc.stdout, proc.stderr


def list_process_capabilities() -> list[str]:
    """Enumerate every capability that has a process model, via
    `rlv-knowledge process --list`. One cap id per stdout line."""
    rc, out, err = _run_bcm_pack_process("--list")
    if rc != 0:
        raise RuntimeError(f"`rlv-knowledge process --list` failed (exit {rc}): {err.strip()}")
    return [ln.strip() for ln in out.splitlines() if ln.strip() and not ln.startswith("#")]


def _model_doc(env: dict, stem: str) -> dict:
    """Return the parsed mapping of one model file from a `rlv-knowledge process`
    envelope. When `.parsed` is null (commands.yaml / read-models.yaml often
    fail strict YAML on `api_binding: { path: /x/{id} }` flow mappings),
    DEGRADE GRACEFULLY to an empty dict — we deliberately do NOT re-parse
    `.raw` with pyyaml, which fails identically."""
    entry = (env.get("model") or {}).get(stem) or {}
    parsed = entry.get("parsed")
    return parsed if isinstance(parsed, dict) else {}


def load_capability(cap_id: str) -> Capability:
    """Fetch and assemble one capability's process model from
    `rlv-knowledge process <cap_id> --compact`."""
    rc, out, err = _run_bcm_pack_process(cap_id, "--compact")
    if rc != 0:
        raise RuntimeError(
            f"`rlv-knowledge process {cap_id}` failed (exit {rc}): {err.strip()}"
        )
    env = json.loads(out)
    agg = _model_doc(env, "aggregates").get("aggregates", []) or []
    cmd = _model_doc(env, "commands").get("commands", []) or []
    pol = _model_doc(env, "policies").get("policies", []) or []
    rm_doc = _model_doc(env, "read-models")
    rms: list[dict] = []
    for p in (rm_doc.get("read_models", []) or []):
        rms.append({"_kind": "projection", **p})
    for q in (rm_doc.get("queries", []) or []):
        rms.append({"_kind": "query", **q})
    bus = _model_doc(env, "bus")
    name = ((env.get("meta") or {}).get("capability") or "") or ""
    return Capability(
        cap_id=cap_id, aggregates=agg, commands=cmd, policies=pol,
        read_models=rms, bus=bus, name=name,
    )


def discover_capabilities(only: str | None = None) -> list[Capability]:
    cap_ids = list_process_capabilities()
    # Only render source-context-prefixed capability ids (e.g.
    # BNK.RLVR.CAP.…), skipping any stale pre-namespacing short forms
    # (CAP.BSP.004.ENV) the CLI may still surface.
    cap_ids = [
        c for c in cap_ids if _CAP_DIR_RE.search(c) and _SRC_CTX_RE.match(c)
    ]
    if only:
        cap_ids = [c for c in cap_ids if c == only]
        if not cap_ids:
            raise RuntimeError(f"No process model matching {only} (via `rlv-knowledge process`)")
    return [load_capability(c) for c in sorted(cap_ids)]



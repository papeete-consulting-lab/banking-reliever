@dataclass
class Widget:
    """A target widget — a row in the desired-state set."""
    artifact_id: str          # stable, unique per process artifact
    kind: str                 # event | command | aggregate | policy | read_model | external_event | frame | connector | label
    label: str
    x: float = 0.0
    y: float = 0.0
    w: float = STICKY_W
    h: float = STICKY_H
    parent_artifact_id: str | None = None
    # Board-level tags to attach to this sticky. Each entry is a tag TITLE
    # (e.g. "RVT.BSP.004.PAYMENT_GRANTED" or "CAP.BSP.004.AUT"); the
    # reconciler resolves titles to ids, creating the tag on the board if
    # it does not already exist.
    tags: list[str] = field(default_factory=list)
    extra: dict[str, Any] = field(default_factory=dict)


@dataclass
class Connector:
    artifact_id: str          # stable id
    from_artifact_id: str
    to_artifact_id: str
    style: str = "solid"      # solid | dashed
    label: str = ""



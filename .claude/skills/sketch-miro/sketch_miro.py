def load_state() -> dict[str, Any]:
    state: dict[str, Any]
    if STATE_FILE.exists():
        try:
            state = json.loads(STATE_FILE.read_text())
        except json.JSONDecodeError:
            print(f"[warn] {STATE_FILE} is corrupt — starting from scratch", file=sys.stderr)
            state = {}
    else:
        state = {}
    # Ensure all expected keys exist for backwards compatibility with
    # state files written by older versions of the script.
    state.setdefault("board_id", None)
    state.setdefault("widgets", {})
    state.setdefault("tags", {})         # {tag_title: tag_id}
    state.setdefault("widget_tags", {})  # {widget_artifact_id: [tag_title, ...]}
    return state


def save_state(state: dict[str, Any]) -> None:
    STATE_FILE.parent.mkdir(parents=True, exist_ok=True)
    STATE_FILE.write_text(json.dumps(state, indent=2, sort_keys=True))


def save_url(url: str) -> None:
    URL_FILE.write_text(url + "\n")


# ──────────────────────────────────────────────────────────────────────────
# Miro REST API client (just the parts we need)
# ──────────────────────────────────────────────────────────────────────────

class MiroClient:
    def __init__(self, token: str, dry_run: bool = False) -> None:
        self.session = requests.Session()
        self.session.headers.update({
            "Authorization": f"Bearer {token}",
            "Accept": "application/json",
            "Content-Type": "application/json",
        })
        self.dry_run = dry_run
        self.api_calls = 0
        self.retries = 0

    def _request(self, method: str, path: str, *, body: dict | None = None) -> dict:
        if self.dry_run and method in {"POST", "PATCH", "DELETE"}:
            return {"id": f"dryrun-{self.api_calls}"}
        url = f"{API_BASE}{path}"
        for attempt in range(5):
            self.api_calls += 1
            resp = self.session.request(method, url, json=body)
            if resp.status_code == 429:
                self.retries += 1
                wait = float(resp.headers.get("Retry-After", "2")) + 0.5 * attempt
                time.sleep(wait)
                continue
            if resp.status_code >= 400:
                raise RuntimeError(
                    f"Miro {method} {path} → {resp.status_code}: {resp.text[:500]}"
                )
            if resp.status_code == 204:
                return {}
            return resp.json()
        raise RuntimeError(f"Miro {method} {path} — exhausted retries on 429")

    # ── Boards ──
    def create_board(self) -> dict:
        return self._request("POST", "/boards", body={
            "name": BOARD_NAME,
            "description": BOARD_DESCRIPTION,
            "policy": {"sharingPolicy": {"access": "private"}},
        })

    def get_board(self, board_id: str) -> dict:
        return self._request("GET", f"/boards/{board_id}")

    def delete_board_widget(self, board_id: str, item_id: str) -> None:
        self._request("DELETE", f"/boards/{board_id}/items/{item_id}")

    # ── Frames ──
    def create_frame(self, board_id: str, *, title: str, x: float, y: float,
                     w: float, h: float, fill_color: str | None = None) -> dict:
        body: dict[str, Any] = {
            "data": {"title": title, "format": "custom", "type": "freeform"},
            "position": {"x": x, "y": y},
            "geometry": {"width": w, "height": h},
        }
        if fill_color:
            body["style"] = {"fillColor": fill_color}
        return self._request("POST", f"/boards/{board_id}/frames", body=body)

    def update_frame(self, board_id: str, item_id: str, *, title: str,
                     fill_color: str | None = None) -> dict:
        body: dict[str, Any] = {"data": {"title": title}}
        if fill_color:
            body["style"] = {"fillColor": fill_color}
        return self._request("PATCH", f"/boards/{board_id}/frames/{item_id}", body=body)

    # ── Sticky notes ──
    def create_sticky(self, board_id: str, *, content: str, color: str, x: float, y: float,
                      w: float, parent_id: str | None) -> dict:
        body: dict[str, Any] = {
            "data": {"content": content, "shape": "square"},
            "style": {"fillColor": color},
            "position": {"x": x, "y": y},
            "geometry": {"width": w},
        }
        if parent_id:
            body["parent"] = {"id": parent_id}
        return self._request("POST", f"/boards/{board_id}/sticky_notes", body=body)

    def update_sticky(self, board_id: str, item_id: str, *, content: str, color: str) -> dict:
        return self._request("PATCH", f"/boards/{board_id}/sticky_notes/{item_id}", body={
            "data": {"content": content, "shape": "square"},
            "style": {"fillColor": color},
        })

    # ── Connectors ──
    def create_connector(self, board_id: str, *, start_id: str, end_id: str,
                         style: str, label: str) -> dict:
        stroke_style = "dashed" if style == "dashed" else "normal"
        body: dict[str, Any] = {
            "startItem": {"id": start_id},
            "endItem":   {"id": end_id},
            "style": {
                "strokeColor": "#1a1a1a",
                "strokeStyle": stroke_style,
                "strokeWidth": "2",
                "endStrokeCap": "arrow",
                "startStrokeCap": "none",
            },
        }
        if label:
            body["captions"] = [{"content": label}]
        return self._request("POST", f"/boards/{board_id}/connectors", body=body)

    def delete_connector(self, board_id: str, item_id: str) -> None:
        self._request("DELETE", f"/boards/{board_id}/connectors/{item_id}")

    # ── Tags (board-level entities that can be attached to items) ──
    def list_tags(self, board_id: str) -> list[dict]:
        """List all tags defined on the board.

        The v2 tags endpoint paginates with `limit` + `offset` (it returns
        `total`, `size`, `offset`, `limit`; no cursor). We loop until we
        have collected `total` items.
        """
        out: list[dict] = []
        offset = 0
        page_size = 50
        while True:
            res = self._request(
                "GET",
                f"/boards/{board_id}/tags?limit={page_size}&offset={offset}",
            )
            chunk = res.get("data", []) or []
            out.extend(chunk)
            total = int(res.get("total", len(out)) or len(out))
            offset += len(chunk)
            if not chunk or offset >= total:
                return out

    def create_tag(self, board_id: str, *, title: str, fill_color: str = "gray") -> dict:
        return self._request("POST", f"/boards/{board_id}/tags", body={
            "title": title,
            "fillColor": fill_color,
        })

    def attach_tag(self, board_id: str, item_id: str, tag_id: str) -> None:
        # Miro v2 attach uses a query parameter, no body.
        self._request("POST", f"/boards/{board_id}/items/{item_id}?tag_id={tag_id}")

    def detach_tag(self, board_id: str, item_id: str, tag_id: str) -> None:
        self._request("DELETE", f"/boards/{board_id}/items/{item_id}?tag_id={tag_id}")


